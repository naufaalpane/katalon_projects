				DAY_ALOC_RATIO_25,
				DAY_ALOC_RATIO_26,
				DAY_ALOC_RATIO_27,
				DAY_ALOC_RATIO_28,
				DAY_ALOC_RATIO_29,
				DAY_ALOC_RATIO_30,
				DAY_ALOC_RATIO_31
			FROM  TB_M_DELIVERY_COEF
			WHERE 
				COMPANY_CD = @@comcd AND
				PROCESS_CD = @@proccd AND
				YYMM = @@yymn AND 
				DEST_CD = @@destcd
		) SRC
		UNPIVOT(
			VALUE
			FOR DAY_ALOC_RATIO IN (
				DAY_ALOC_RATIO_01,
				DAY_ALOC_RATIO_02,
				DAY_ALOC_RATIO_03,
				DAY_ALOC_RATIO_04,
				DAY_ALOC_RATIO_05,
