			)
		) PVT
	) [SOURCE]
	WHERE DAY_ALOC_RATIO BETWEEN ''DAY_ALOC_RATIO_01'' AND ''DAY_ALOC_RATIO_'+ CAST(@@NumberOfDays AS VARCHAR) +'''
'; 
EXECUTE sp_executesql @@QuerySelect, @@Params, @@comcd = @company_cd, @@proccd = @process_cd, @@yymn = @yearmonth, @@destcd = @dest_cd