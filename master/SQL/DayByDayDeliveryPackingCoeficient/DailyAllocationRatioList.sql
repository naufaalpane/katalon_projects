				DAY_ALOC_RATIO_08,
				DAY_ALOC_RATIO_09,
				DAY_ALOC_RATIO_10,
				DAY_ALOC_RATIO_11,
				DAY_ALOC_RATIO_12,
				DAY_ALOC_RATIO_13,
				DAY_ALOC_RATIO_14,
				DAY_ALOC_RATIO_15,
				DAY_ALOC_RATIO_16,
				DAY_ALOC_RATIO_17,
				DAY_ALOC_RATIO_18,
				DAY_ALOC_RATIO_19,
				DAY_ALOC_RATIO_20,
				DAY_ALOC_RATIO_21,
