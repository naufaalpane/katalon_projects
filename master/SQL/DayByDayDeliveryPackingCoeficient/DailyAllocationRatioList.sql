				DAY_ALOC_RATIO_21,
				DAY_ALOC_RATIO_22,
				DAY_ALOC_RATIO_23,
				DAY_ALOC_RATIO_24,
				DAY_ALOC_RATIO_25,
				DAY_ALOC_RATIO_26,
				DAY_ALOC_RATIO_27,
